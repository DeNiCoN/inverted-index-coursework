use clap::Parser;
use inverted_index_coursework::rpc::InvertedIndexServiceClient;
use std::{
    net::{IpAddr, Ipv6Addr, SocketAddr},
    time::Duration,
};
use tarpc::{client, context, tokio_serde::formats::Json};
use tokio::time::sleep;
use tracing::Instrument;

#[derive(Parser)]
struct Flags {
    #[clap(long)]
    get: Option<String>,
    #[clap(long)]
    build: Option<String>,
}

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    let flags = Flags::parse();

    let transport = tarpc::serde_transport::tcp::connect(
        (IpAddr::V6(Ipv6Addr::LOCALHOST), 5545),
        Json::default,
    );

    // WorldClient is generated by the service attribute. It has a constructor `new` that takes a
    // config and any Transport as input.
    let client =
        InvertedIndexServiceClient::new(client::Config::default(), transport.await?).spawn();

    if let Some(build_path) = flags.build {
        let hello = async move {
            // Send the request twice, just to be safe! ;)
            tokio::select! {
                hello1 = client.build(context::current(), vec![build_path]) => { hello1 }
            }
        }
        .await;
        println!("{:?}", hello)
    } else if let Some(word) = flags.get {
        let result = async move {
            // Send the request twice, just to be safe! ;)
            tokio::select! {
                hello1 = client.get(context::current(), word) => { hello1 }
            }
        }
        .await;
        println!("{:?}", result)
    }

    // Let the background span processor finish.
    sleep(Duration::from_micros(1)).await;

    Ok(())
}
